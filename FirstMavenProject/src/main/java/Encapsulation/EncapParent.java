package Encapsulation;

public class EncapParent {

	private String name = "Raj";
	private int age = 20;
	
	public void setName(String name) 
	{
		this.name = name;
	}
	
	public String getName()
	{
		return name;
	}
	
	public void setAge(int age) 
	{
		this.age = age;
	}
	
	public int getAge()
	{
		return age;
	}
}
